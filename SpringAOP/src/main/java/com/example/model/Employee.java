package com.example.model;

public class Employee {

	private String empId;
	
	private String name; 
	
	public Employee() {}

	public String getEmpId() {
		return empId;
	}

	public String getName() {
		return name;
	}

	public void setEmpId(String empId) {
		this.empId = empId;
	}

	public void setName(String name) {
		this.name = name;
	} 
	
	
}
